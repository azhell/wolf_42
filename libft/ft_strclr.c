#include "libft.h"

void	ft_strclr(char *s)
{
	size_t i;
	size_t size;

	i = 0;
	if (s == NULL)
		return ;
	size = ft_strlen(s);
	while (i < size)
	{
		s[i] = '\0';
		i++;
	}
}
